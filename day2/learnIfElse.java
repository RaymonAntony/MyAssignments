public class learnIfElse {

	public static void main(String[] args) {
	 int num=4;
	 if(num>0) {
		 System.out.println("Given number is Positive: "+num);
	 }
	 else if(num==0)
		 System.out.println("Neutral: "+num);
	 else {
		 System.out.println("Given number is Negative: "+num);	 
	 }

	}

}
